/**
 * @module
 */

/**
 * Adds the specified operands
 * @type {Function}
 * @param {number} x - First Operand
 * @param {number} y - Second Operand
 * @returns {number}
 */
 exports.add = (x, y) => x + y;

/**
 * Substract the specified operands
 * @type {Function}
 * @param {number} x - First Operand
 * @param {number} y - Second Operand
 * @returns {number}
 */
 exports.substract = (x, y) => x - y;

/**
 * Multiply the specified operands
 * @type {Function}
 * @param {number} x - First Operand
 * @param {number} y - Second Operand
 * @returns {number}
 */
 exports.multiply = (x, y) => x * y;

/**
 * Divides the specified operands
 * @type {Function}
 * @param {number} x - First Operand
 * @param {number} y - Second Operand
 * @returns {number}
 */
 exports.divide = (x, y) => x / y;